res=Print+print3
print("El resultado de la suma es:",res)

print("Resta")
res=Print-print3
print("El resultado de la resta es:",res)

print("Multiplicación")
res=Print*print3
print("El resultado de la multiplicación es:",res)

print("Exponenete")
res=Print**print3
print("El resultado del exponente es:",res)

print("División")
res=Print/print3
print("El resultado de la división es:",res)

print("Residuo")
res=Print%print3
if(res==0):
    print("El residuo de la división es:",res)
else:
    print("El residuo de la división es:",res)

print("División entera")
res=Print//print3
